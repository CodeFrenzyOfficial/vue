            <h3 class="h3 section-title">Research Overview:</h3>
            <p>
              I am interested in identifying and characterizing novel drivers in
              cancer with the goal of discovering novel therapeutic targets. The
              main focus of my work in recent years has been to understand the
              interplay between signal transduction and gene regulation
              (transcription and epigenetics) in human pancreatic ductal
              adenocarcinoma (pancreatic cancer) cells, and how this interplay
              drives this disease. A major thrust of my research focuses on
              elucidating which signaling and transcriptional pathways regulate
              the biological properties of pancreatic cancer stem cells, in
              particular, programs that control pancreatic cancer stem cell
              self-renewal and differentiation. Additionally, I am interested in
              how pancreatic cancer cells overcome oncogene ablation, in
              particular, ablation of mutant KRAS-signaling. Much of my research
              is centered on the transcription factor HNF1A, which I have
              identified as a novel oncogene in pancreatic cancer that drives
              both pancreatic cancer stem cells and resistance to KRAS ablation.
              I aim to uncover how HNF1A regulates these biological processes in
              pancreatic cancer, as well as understanding how HNF1A itself is
              regulated in an effort to block its function for therapeutic gain.
            </p>

