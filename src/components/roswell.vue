            <h3 class="h3 section-title">Biography:</h3>
            <h5>Pre-graduate Research Experience</h5>

            <p>
              During my time in college at the State University of New York
              (SUNY) at Albany, I pursued a number of undergraduate research
              opportunities. In the summer of 2003, I was accepted into the
              Research for Undergraduates (REU) program, a 10-week research
              program run by the Wadsworth Center of the New York State Health
              Department. My research, under the mentorship of Dr. Ron
              Limberger, PhD, was examining the impact of genetic ablation of
              motility gene, orf4, in the spirochete bacteria Treponema
              denticola. During my time in the lab, I learned a number of
              microbiology and molecular biology techniques, including bacteria
              culture, RNA/DNA isolation, and quantitative reverse transcriptase
              PCR (qRT-PCR).
            </p>

            <p>
              Following my participation in the REU program, I worked for two
              semesters in the lab of Dr. David Shub, PhD at SUNY Albany. My
              project entailed creating and characterizing a conditional
              knockout of a gene of unknown function, at the time called gene
              60.1, in T4 bacteriophage. I learned a number of molecular biology
              techniques during this time, including gene cloning, site-directed
              mutagenesis, and radiolabeled DNA:DNA hybridization. My
              contribution to this project lead to my inclusion as a middle
              author in a paper published in Proceedings of the National Academy
              of Sciences of the United States of America.
            </p>

            <p>
              After receiving my Bachelor’s degree in Biology in 2004, I joined
              the lab of Dr. Andrew Aplin, PhD at Albany Medical College as a
              research technician. Being a relatively new lab at the time, I was
              fortunate to have a great deal of research responsibility, which
              included helping the graduate students and postdoctoral fellow in
              the lab with their studies. I was also given my own exclusive
              project, looking at the role of focal adhesion kinase (FAK) in
              melanoma. During my time as a technician, I mastered a number of
              techniques that I would use through my graduate year. These
              included Western blotting, qRT-PCR, cell migration and invasion
              assays (scrape wound and Boyden chamber), immunofluorescence, cell
              culture, primary human cell isolation, flow cytometry, cell
              cycle/proliferation and apoptosis assays, transient and stable
              transfection, RNA-interference, and extensive cloning of genes and
              shRNAs into and packaging of lentiviral and adenoviral vectors.
            </p>

            <h5>Doctoral Dissertation</h5>

            <p>
              I received my PhD in Genetics from Thomas Jefferson University in
              2012 while training in the lab of Dr. Andrew Aplin. My graduate
              studies focused on the stem cell and neural crest-associated
              transcription factor, FOXD3, which I found to be expressed in
              human melanoma cells. I found that FOXD3 is only expressed in
              melanoma cells harboring activating mutations in the kinase, BRAF,
              and that FOXD3 protein and mRNA levels rapidly and dramatically
              increase upon inhibition of the MAPK signaling cascade. I went on
              to demonstrate that overexpression of FOXD3 led to a potent G0/G1
              arrest in melanoma, suggesting that FOXD3 might be a novel tumor
              suppressor. Finally, I demonstrated that this growth arrest is
              mediated by upregulation of p21 in a p53-dependent manner.
              Following publication of this study in Cancer Research in 2010, I
              then examined the role of FOXD3 in the response of melanoma to
              vemurafenib. In collaboration with fellow graduate student Kevin
              Basile, we found that upregulation of FOXD3 by BRAF-inhibitors
              protects melanoma from apoptosis (published in Oncogene in 2012).
              Using microarrays and ChIP-seq, I identified the neuregulin
              receptor, ERBB3, as a direct target of FOXD3. I demonstrated that
              ERBB3 expression is dependent on FOXD3, and that activation of
              ERBB3 promotes potent AKT signaling, protecting melanoma cells
              from BRAF inhibition. I found that elevated ERBB3 activation in
              both xenografts and in patients treated with vemurafenib, and that
              inhibition of ERBB3 or its partner, ERBB2, synergized with
              vemurafenib to block tumor growth in vivo. These results were
              published in The Journal of Clinical Investigation in 2013.
              Serving as the basis of my thesis, I concluded that FOXD3 is a
              transcription factor with context-dependent roles in melanoma: 1)
              a growth suppressor in that is actively repressed by mutant BRAF
              signaling and 2) a pro-survival factor when BRAF signaling is
              inhibited, through regulation of ERBB3. Additionally, in
              collaboration with fellow Aplin lab member Dr. Michele Weiss, we
              found that FOXD3 also transcriptionally represses the EMT
              transcription factor TWIST1 in melanoma cells (Molecular Cancer
              Research), and that TWIST1 promotes invasion of melanoma cells by
              upregulation of MMP1 (Cancer Research). These results highlighted
              the interplay between oncogenic signaling pathways and
              transcriptional regulation in melanoma.
            </p>

            <h5>Postdoctoral Research</h5>

            <p>
              Following my graduate studies, I became acutely fascinated by
              pancreatic ductal adenocarcinoma (PDA) due to its abysmal
              prognosis (5-year survival of 9%), its association with
              hyperactivation of MAPK signaling through KRAS mutations (&gt;90%
              of cases), and it's poorly understood transcriptional networking.
              For my postdoctoral fellowship, I joined the labs of Dr. Diane
              Simeone in 2012 and Dr. Howard Crawford in 2017 at the University
              of Michigan with a goal to gain better insight into the interplay
              between signal transduction and transcription factors in PDA. I
              chose to apply this focus to understanding the biology of
              pancreatic cancer stem cells (PCSCs), a notoriously
              drug-resistant, tumor-driving subpopulation of cancer cells for
              which there are no effective clinical options. Initially, I
              focused on the role of the NOTCH pathway in maintaining the PCSC
              state, showing that multiple components of the NOTCH pathway are
              elevated in PCSCs and are critical for maintaining these cells.
              Targeting this pathway with clinically available γ-secretase
              inhibitors depletes PCSCs both alone and in combination with the
              conventional chemotherapeutic agent gemcitabine (Abel et al, 2014,
              PLoS ONE). Following this study, I identified the
              endodermal-lineage transcription factor HNF1A as a major regulator
              of the PCSC state using microarray analysis of PCSCs isolated from
              patient-derived PDA cells. I found that knockdown of HNF1A
              potently inhibits PDA cell growth, induces apoptosis, and depletes
              PCSCs both in vitro and in vivo (Abel et al, 2018, eLife).
              Additionally, ectopic expression of HNF1A increases PCSC numbers
              in PDA cells, while promoting cellular transformation in concert
              with mutant KRAS in normal pancreatic ductal cell lines. Using
              ChIP-seq and modified RNA-seq (Bru-seq) I found that the critical
              target of HNF1A is the stem cell transcription factor OCT4.
              Additionally, HNF1A also regulates a multitude of genes associated
              with poor survival in PDA. Having generated such a robust data set
              for HNF1A in PDA, together with so little being known about its
              roles, has made HNF1A a major focal point of my proposed research
              in my independent research career.
            </p>

            <p>
              In addition to honing the research techniques acquired prior to my
              postdoctoral training, I acquired additional skills in the Simeone
              and Crawford labs. New skills I have acquired include working with
              rodents (housing, husbandry, euthanasia), subcutaneous and
              orthotopic (pancreas) xenotransplantation, limiting dilution
              assays (for measuring cancer stem cells), in vivo bioluminescent
              imaging, tumorsphere assays, immunohistochemistry, establishing
              primary cell lines (normal and cancer, human and mouse), analyzing
              patient data, CRISPR/Cas9-guided DNA editing/transcriptional
              regulation, and variant forms of RNA-seq (Bru-seq, BruChase-seq,
              BruUV-seq, and BruDRB-seq) to measure transcription, RNA turnover,
              enhancer/promoter activity, and RNA elongation rates in
              collaboration with Dr. Mats Ljungman (University of Michigan).
              Additionally, I have had the opportunity to work hands-on with
              bioinformatician Dr. Brian Magnuson (University of Michigan), and
              have become proficient in analyzing RNA-seq/ChIP-seq data.
            </p>

            <h5>Summary of Research Accomplishments</h5>

            <p>
              In addition to my 3 first author publications, I have coauthored
